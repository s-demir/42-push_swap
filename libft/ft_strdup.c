#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	int		j;
	int		i;

	i = 0;
	j = ft_strlen((char *)s1);
	a = (char *)malloc((j * sizeof(char)) + 1);
	if (!a)
		return (NULL);
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
