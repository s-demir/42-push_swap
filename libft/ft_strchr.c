char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != (char)c)
	{
		if (s[i] == '\0' && s[i] != (char)c)
			return (0);
		i++;
	}
	return ((char *)s + i);
}
